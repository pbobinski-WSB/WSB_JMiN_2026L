int ladder(int input) {
    if (input == 0) return 10;
    else if (input == 1) return 20;
    else if (input == 2) return 30;
    else if (input == 3) return 40;
    else if (input == 4) return 50;
    else if (input == 5) return 60;
    else if (input == 6) return 70;
    else return -1;
}

